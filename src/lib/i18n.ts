export type TranslationKey = keyof typeof strings.fr;

const VALID_LOCALES: Locale[] = ['fr', 'en', 'nl', 'es'];

export function getLocale(): Locale {
  const stored = localStorage.getItem(LOCALE_KEY);
  if (VALID_LOCALES.includes(stored as Locale)) return stored as Locale;
  return 'fr';
}

export function setLocale(locale: Locale): void {
  localStorage.setItem(LOCALE_KEY, locale);
}

export function t(key: TranslationKey, locale?: Locale): string {
  const loc = locale ?? getLocale();
  const table = strings[loc] as Record<TranslationKey, string>;
  return table[key] ?? strings.fr[key];
}

export function getDateLocale(locale: Locale): string {
  const map: Record<Locale, string> = {
    fr: 'fr-FR',
    en: 'en-US',
    nl: 'nl-NL',
    es: 'es-ES',
  };
  return map[locale];
}
